package com.neuedu.a;

public class Zuoye05 {
    public static void main(String[] args) {
        int a = 0;
        for (int i = 2; i <= 100; i += 2) {
            a += i;
        }
        System.out.println("1到100的偶数和为：" + a);
        int b = 0;
        for (int i = 1; i <= 100; i += 2) {
            b += i;
        }
        System.out.println("1到100的奇数和为：" + b);
         }
        }


